package array;

public class Rect {

	int x;          //矩形左上角x坐标
    int y;          //矩形左上角y坐标
    int width;      //矩形宽度
    int height;     //矩形高度
	public Rect(int x, int y, int width, int height) {
		super();
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
    
}
